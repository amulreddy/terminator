package com.autowares.mongoose.model;

public class Shop {
	
	private String storeAccountNumber;
	private String autowaresAccountNumber;
	private String businessName;
	
	public String getStoreAccountNumber() {
		return storeAccountNumber;
	}
	public void setStoreAccountNumber(String storeAccountNumber) {
		this.storeAccountNumber = storeAccountNumber;
	}
	public String getAutowaresAccountNumber() {
		return autowaresAccountNumber;
	}
	public void setAutowaresAccountNumber(String autowaresAccountNumber) {
		this.autowaresAccountNumber = autowaresAccountNumber;
	}
	public String getBusinessName() {
		return businessName;
	}
	public void setBusinessName(String businessName) {
		this.businessName = businessName;
	}

}
